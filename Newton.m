+c(11)*t.*(t-1).*(t-2).*(t-3).*(t-4).*(t-5).*(t-6).*(t-7).*(t-8).*(t-9)...
;
e=abs((f-Nn)./f);
Em=-1;
for i=1:length(x),
    if Em<e(i),
        Em=e(i);
        xm=-1+i*0.001;
    end
end

plot(x,f);
hold on;
plot(x,Nn);
legend('f(x)','Nn(x)');
